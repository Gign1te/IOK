class MaterialPoint:
    def __init__(self, point_id, coord_x, coord_y, velocity_x, velocity_y, x_0, y_0, time):
        self.id = point_id
        self.coord_x = coord_x
        self.coord_y = coord_y
        self.velocity_x = velocity_x
        self.velocity_y = velocity_y
        self.x_0 = x_0
        self.y_0 = y_0
        self.time = time
